    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       
        Datos d = new Datos();
        
        String nombreMedicina = MedicinaNombreInsert.getText();
        String nombreNota = NotaInsert.getText();
        String hora = String.valueOf(Integer.valueOf(jSpinner2.getValue().toString()));
        String minutos = String.valueOf(Integer.valueOf(jSpinner1.getValue().toString()));
        
        try {
            try (BufferedWriter escribirTXT = new BufferedWriter(new FileWriter("Medicina.txt", true))) {


                escribirTXT.write(nombreMedicina + "," + nombreNota + "," + hora + "," + minutos + "\n");
                escribirTXT.close();
                JOptionPane.showMessageDialog(null, "Registro Exitoso", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException ex) {
            System.out.println("problema al crear el  archivo de datos");
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

