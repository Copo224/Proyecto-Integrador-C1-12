    public void readFile(String filename) {

        int arrayLength = 4;

        try {
            File file = new File(filename + ".txt");
            FileReader fRead = new FileReader(file);
            BufferedReader bRead = new BufferedReader(fRead);
            String str;
            int ln = 0;
            int j = 0;
            while ((str = bRead.readLine()) != null) {
                StringTokenizer tokenizer = new StringTokenizer(str, ",");
                String[] content = new String[arrayLength];
                for (int i = 0; tokenizer.hasMoreTokens(); i++) {
                    content[i] = tokenizer.nextToken();
                    tableMedicinas.setValueAt(content[i], j, i);
                }
                j++;
            }

            showModal("Lineas: " + ln);

        } catch (Exception e) {

        }

    }

    public boolean findCredentials(String filename, String username, String password) {
        int found = 0;
        try {
            // Crea el archivo.
            File file = new File(filename + ".txt");
            FileReader fileReader = new FileReader(file);
            BufferedReader bufferedReader = new BufferedReader(fileReader);

            String line;
            String[] users = new String[6];

            // Lee el archivo.
            while ((line = bufferedReader.readLine()) != null) {
                StringTokenizer st = new StringTokenizer(line, ",");

                // Separa por comas al String en pequeÃ±os trozos (tokens)
                // y los almacena en un Array.
                for (int i = 0; (st.hasMoreTokens()); i++) {
                    users[i] = st.nextToken();
                }

                // Comprar parametros recibidos con contenido del archivo.
                if (users[1].equals(username) && users[5].equals(password)) {
                    found++;
                }
            }
        } catch (IOException e) {
            showModal("Exception: " + e);
        }
        // Devuelve true o false.
        return found > 1;
    }

    public String showCredentials(String fcUserId) {

        String userid = fcUserId;
        String[] founds = new String[6];

        try {
            File file = new File("file.txt");
            FileReader fileReader = new FileReader(file);
            BufferedReader bufferedReader = new BufferedReader(fileReader);

            String line;
            String[] users = new String[6];

            while ((line = bufferedReader.readLine()) != null) {
                StringTokenizer st = new StringTokenizer(line, ",");

                for (int i = 0; (st.hasMoreTokens()); i++) {
                    users[i] = st.nextToken();
                }

                if (users[0].equals(userid)) {
                    founds = users;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Exception: " + e);
        }

        return founds[0]
                + "\n"
                + founds[1]
                + "\n"
                + founds[2]
                + "\n"
                + founds[3]
                + "\n"
                + founds[4]
                + "\n"
                + founds[5];
    }

